package com.java.loops;

public class Do_While {

	public static void main(String[] args) {
		int num = 6;

		do {
			System.out.println(num);
			num++;
		} while (num < 6);
		
		
		
		System.out.println("=============");

		int count = 1;

		do {
			System.out.println(count);
			count++;
		} while (count < 25);

	}

}
