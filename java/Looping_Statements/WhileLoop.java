package com.java.loops;

public class WhileLoop {

	public static void main(String[] args) {
		int num = 1;

		while (num < 6) {
			System.out.println("num: " + num);
			num++;
		}
		
		
		System.out.println("=================");

		int count = 1;

		while (count < 25) {
			System.out.println("count : " + count);
			count++;
		}

	}

}
