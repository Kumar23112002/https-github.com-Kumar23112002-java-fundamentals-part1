package com.java.loops;

public class For_Loop {

	public static void main(String[] args) {

		for (char ch = 'A'; ch <= 'E'; ch++) {
			System.out.println("char: " + ch);
		}
		System.out.println("=======================");
		for (int k = 1; k < 4; k++) {
			System.out.println("value = " + k);
		}
	}

}
