package com.java.loops;

public class Example_Loop {

	public static void main(String[] args) {
		int num = 1;
		while (num < 7) {
			System.out.println(num + "A");
			num++;
		}
		
		System.out.println("================");
		
		
		int n = 1;
		do {
			System.out.println(n + "A");
			n++;
		} while (n < 7);

	}

}
